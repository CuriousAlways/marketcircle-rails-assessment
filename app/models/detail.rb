class Detail < ApplicationRecord
  TITLE = ['Dr.', 'Mr.', 'Mrs.', 'Prof.'].freeze
  #-------------------------------------------------------------------------------------
  # Associations
  #-------------------------------------------------------------------------------------
  belongs_to :person

  #-------------------------------------------------------------------------------------
  # Validations
  #-------------------------------------------------------------------------------------
  validates :email, presence: true, uniqueness: true, format: { with: ::URI::MailTo::EMAIL_REGEXP }
end
